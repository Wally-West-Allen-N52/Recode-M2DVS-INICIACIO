package javaVariavel01;

public class DeclaracaoDeVariaveis01 {

	public static void main(String[] args) {
		
		// camel case
		int quantProduto; // Não iniciada
		float precoProdto = 35.50f; // variável iniciada
		
		quantProduto = 12; // Variével iniciada
		
		System.out.println("Quntidade de produtos: " + quantProduto
				+ "\nPreço do produtos: $" + precoProdto);
	}

}
